    private int generateNewInvoiceId() {
        String query = "SELECT MAX(invoice_id) AS max_id FROM Invoice";
        try ( Connection conn = DBConnection.getConnection();  PreparedStatement stmt = conn.prepareStatement(query);  ResultSet rs = stmt.executeQuery()) {

            if (rs.next()) {
                int maxId = rs.getInt("max_id");
                return maxId + 1;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to generate new Invoice ID.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return 1;
    }


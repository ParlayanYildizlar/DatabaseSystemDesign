    private void populateInvoiceDetails() {
        subsTypeTxtField.setText(subscriptionType);
        nameTxtField.setText(name);
        surnameTxtField.setText(surname);

        int newInvoiceId = generateNewInvoiceId();
        invoiceIdTxtField.setText(String.valueOf(newInvoiceId));

        double totalAmount = calculateTotalAmount(subscriptionId);
        totalAmountTxtField.setText(String.format("%.2f", totalAmount).replace(',', '.'));
    }
    private void approveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveButtonActionPerformed
        int newInvoiceId = generateNewInvoiceId();
        String totalAmount = totalAmountTxtField.getText();

        String query = "INSERT INTO Invoice (invoice_id, subscription_id, issue_date, due_date, total_amount, payment_status) "
                + "VALUES (?, ?, GETDATE(), DATEADD(DAY, 30, GETDATE()), ?, 'Unpaid')";

        try ( Connection conn = DBConnection.getConnection();  PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setInt(1, newInvoiceId);
            stmt.setInt(2, subscriptionId);
            stmt.setDouble(3, Double.parseDouble(totalAmount));

            int rowsAffected = stmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Invoice approved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to approve invoice.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private double calculateTotalAmount(int subscriptionId) {
        String query = "SELECT unit_price FROM Pricing "
                + "JOIN Subscription_Type ON Pricing.subscription_type_id = Subscription_Type.subscription_type_id "
                + "JOIN Subscription ON Subscription.subscription_type_id = Subscription_Type.subscription_type_id "
                + "WHERE Subscription.subscription_id = ?";
        try ( Connection conn = DBConnection.getConnection();  PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setInt(1, subscriptionId);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                double unitPrice = rs.getDouble("unit_price");
                int randomUsage = (int) (Math.random() * (500 - 10 + 1) + 10);
                double totalAmount = unitPrice * randomUsage;

                System.out.println("Unit Price: " + unitPrice + ", Random Usage: " + randomUsage + ", Total Amount: " + totalAmount);

                return totalAmount;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        System.out.println("Failed to calculate total amount!");
        return 0.0;
    }//GEN-LAST:event_approveButtonActionPerformed

